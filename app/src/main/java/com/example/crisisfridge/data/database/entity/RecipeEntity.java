package com.example.crisisfridge.data.database.entity;

import androidx.room.Entity;
import androidx.room.PrimaryKey;

import com.example.crisisfridge.data.model.dataModel.Recipe;

@Entity(tableName = "recipe")
public class RecipeEntity implements Recipe {

    @PrimaryKey
    private int id;
    private String name;
    private String description;

    public RecipeEntity(int id, String name, String description) {
        this.id = id;
        this.name = name;
        this.description = description;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public String getDescription() {
        return description;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        RecipeEntity that = (RecipeEntity) o;

        if (id != that.id) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;
        return description != null ? description.equals(that.description) : that.description == null;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + (description != null ? description.hashCode() : 0);
        return result;
    }
}
